        <div class="section-header">
          <h2 class="section-title">📚 复旦与伯克利教与学的区别</h2>
        </div>

        <p>伯克利有一个有意思的现象，那就是教室很大，人却冷冷清清。</p>
        
        <p>当然了，不是所有课都这样，但伯克利基本上都是大课。本科生高年级的某些课程，研究生一些课程会是小课（人数小于100人的就可以认为是小课了）。本科生的大部分课程，几乎都是百人，甚至千人大课。</p>
        
        <p>为什么伯克利每一门课程的人数会那么多？因为一个课一学期只会开一次，由一组老师来上。对比之下，复旦的每一门课，比如数学分析会有A和B, 高等数学会有A,B,C,D. 每一个细分的课程都会有五六个老师可以选择，老师这种稀缺资源的投入就非常多了。</p>
        
        <p>在伯克利，既然一个老师可能对应着几百个学生（最多的一个老师对应着1300多个学生），很显然精力是应付不过来。</p>
        
        <p>所以伯克利课程的运作是很依赖助教的，一门几百人的大课对应着20-30人的助教团队。助教是便宜而廉价的，但效果却出奇的好。助教一般是研究生，是从世界各地来的研究生，有硕士也有Phd，基本上在专业领域的水平都很高，教本科生是绰绰有余的。在伯克利，几乎一半的助教都是华人面孔，甚至可以说中国人对教育的重视以及湾区这里华人的广泛，让伯克利几乎变成了一个中文无障碍学校（很多时候可以用中文和助教交流问题）。</p>
        
        <p>当然了，助教的水平高是一方面，制度对于助教投入的约束也是另一方面。我们中国大学的助教水平也很高，但是有些时候并不是太负责，对于课程不会投入很多精力。这边的助教每周都要举办Office Hour, 回答学生的问题，哪怕没有学生的话也必须在线及时回答问题。另外，学生每周还会填写反馈表，里面就有对于助教的评价。这些固定的（助教需要举办office hour)和变化的考核也就促使这些聪明的助教为学生发挥他们的聪明才智。</p>
        
        <p>这种制度设计可能也和国情有关。在中国，一个老师上一节课的薪酬可能是几十人民币。辛辛苦苦讲课拿不到多少钱，所以大多老师没有动力去把课讲好，助教就更没有动力在课程中投入太多精力了。教学只是一个义务的履行，而不是什么有动力的活。</p>
        
        <p>已经解释完一开始的教室很大了，现在又要解释人冷冷清清了。</p>
        
        <p>伯克利的老师一般不点名，而且很多课都有视频回看。所以不去教室上课也有很多途径来补齐上课讲授的知识。</p>
        
        <p>伯克利有很多经典的计算机课程，开设多年，课程建设完善。这些课都有自己的课程网站，过去每一年开课的内容都会收录在网站中，信息公开，包括过去的课件、考试试卷等等。所以学生们能掌握到的课程资源是非常丰富详细的，而且学习路径清晰。</p>
        
        <p>这些计算机课程都会设置Ed平台，这是一个供学生和老师助教、学生和学生之间交流的平台。老师或助教会把每周要完成的任务及重要通知发在Ed上，学生们可以自由提问，助教或其他同学都会及时解答。一切问题都可以在这个网络平台上问，不限时间，不限地点，所有或大或小的问题都能得到快速的回应，这很有益于学生们掌握课堂知识。</p>
        
        <p>课程建设完善还有一个体现是在作业方面。伯克利的计算机课程的作业（包括homework, Lab, project）都会在课程网站发布，描述详细，任务导向明确。并且伯克利有专门的作业提交网站Gradscope，很多计算机作业能上传到网站后立刻获得autograder自动评分，有助于学生们对作业先进行自我检查。然后，得益于强大且人数众多的助教团队，每门课的每次作业都会被批改。</p>
        
        <p>此外，这些课程的评分细则也是公开且详实的。作业、项目、期中、期末、课堂参与...每个部分占比多少都会清晰标出，期末的时候会给学生展示在每个部分的最终得分。除了公布考试成绩和每题得分，助教还会提供班级成绩的最高分最低分、平均分、中位数、标准差，帮助你掌握班级水平的大概分布。我觉得这样的公开透明很好，在国内很多课只能知道自己最终的绩点，而不知道考试具体考了多少分，更不知道自己错在哪里。</p>
        
        <p>这边的课程也是宽容的。基本上每门课都会有slip days或者extension，也就是允许你有时候无法及时完成作业，给你几天宽限的时间。有的课甚至还有成绩覆盖机制，如果你期中考差了而期末考得不错，就允许你用期末成绩覆盖期中成绩的占比。这些宽容的机制给了学生更小的压力和更大的自由，但又是宽而不松的，所以在缓解学生压力的同时又能保证学生们不会把这门课水过去。</p>
        
        <p>讨论了这么多伯克利课程的优点，但我们必须要明确，这些优点大多数体现在伯克利的计算机课程上。伯克利是知名的计算机强校，计算机建设历史悠久，课程设置完善，所以有了以上所说的诸多优点。而其他系的有些课则可能不具有这些优点，比如一些经济学课程的助教稀少，不够负责，不过这终归还是少数。</p>
        
        <p>伯克利与复旦的课程体系差异，本质上是两种教育哲学在全球化时代碰撞的缩影。伯克利的"规模化教育"背后，暗含了一种工业化思维——通过标准化流程和分工（教授负责内容生产，助教负责知识传递，技术工具负责反馈评估）实现教育效率的飞跃，这与硅谷"可扩展性"（scalability）的技术逻辑一脉相承；而国内传统的分班制则更接近农耕文明的"精耕细作"，试图以人力密集体制维持教学的精细度。两种模式各有历史合理性，但在知识爆炸和人才竞争全球化的今天，都面临挑战：前者可能因过度依赖流程而弱化人文关怀，后者则易陷入资源内耗的困境。</p>
        
        <p>更深层的问题在于，教育的核心目标是否需要重新定义。当伯克利用Autograder和Ed平台将学习拆解为"问题-答案"的即时闭环时，知识获取变得高效，但"慢思考"的空间（如批判性思维、跨学科联结）是否被压缩？当国内高校仍将课堂考勤作为学习纪律的象征时，这种"在场感"究竟是维护了教育仪式，还是掩盖了创新动力的不足？未来的课程体系或许需要跳出"规模vs精细""自由vs管控"的二元对立，转向一种适应性学习生态——既能通过技术工具实现基础知识的规模化覆盖，又能通过制度设计（如导师制、跨学科项目）保留深度思考的土壤。而无论东方或西方，教育的终极试金石或许在于：它是否能让学习者在面对未知的复杂性时，依然保有探索的勇气与创造的喜悦。</p>
